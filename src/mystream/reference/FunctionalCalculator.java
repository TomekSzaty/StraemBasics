package mystream.reference;

public class FunctionalCalculator {

    public static double multiplyAandb (double a, double b) {
        return a * b;
    }
    public static double addAandB(double a, double b) {
        return a + b;
    }
    public static double divideAandB (double a, double b) {
        return a / b;
    }
    public static double subtractAandB (double a, double b) {
        return a - b;
    }
    public static double moduloAandB (double a, double b) {
        return a * b;
    }
}
